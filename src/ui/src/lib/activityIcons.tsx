/**
 * Get icon component for activity type
 */
export function getActivityIcon(type: string): React.ComponentType<IconProps> {
  switch (type) {
    case 'feeding':
      return FeedingIcon;
    case 'walk':
      return WalkIcon;
    case 'play':
      return PlayIcon;
    case 'grooming':
      return GroomingIcon;
    case 'vet':
      return VetIcon;
    case 'medication':
      return MedicationIcon;
    case 'training':
      return TrainingIcon;
    default:
      return OtherIcon;
  }
}
